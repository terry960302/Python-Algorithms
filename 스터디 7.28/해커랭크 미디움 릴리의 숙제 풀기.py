def lilyshomework(arr):
    count=0
    while True:
        if min(arr) != arr[0]:
            arr[arr.index(min(arr))], arr[0] = arr[0], arr[arr.index(min(arr))]
            count+=1
        elif max(arr) != arr[-1]:
            arr[arr.index(max(arr))], arr[-1] = arr[-1], arr[arr.index(max(arr))]
            count+=1
        elif arr != sorted(arr):
            arr.remove(min(arr)) and arr.remove(max(arr))
        else:
            break
    return count
   
    




