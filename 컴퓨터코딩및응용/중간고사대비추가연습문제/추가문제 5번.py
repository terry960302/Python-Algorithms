#추가연습문제 5번

word=input('Enter one word: ')
L=[]

for i in word:
    L.append(i)    
print('vowel a: {}'.format(L.count('a')))
print('vowel e: {}'.format(L.count('e')))
print('vowel i: {}'.format(L.count('i')))
print('vowel o: {}'.format(L.count('o')))
print('vowel u: {}'.format(L.count('u')))

    
